function parsePo(text) {
  const map = {};
  const re = /msgid\s+"((?:\\.|[^"\\])*)"\s*msgstr\s+"((?:\\.|[^"\\])*)"/g;
  let m;
  while ((m = re.exec(text))) {
    const id = JSON.parse(`"${m[1]}"`);
    const str = JSON.parse(`"${m[2]}"`);
    if (id) map[id] = str;
  }
  return map;
}

function escapePo(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n");
}

function writePo(locale, entries) {
  const lines = [
    'msgid ""',
    "msgstr \"\"",
    `"Language: ${locale}\\n"`,
    '"MIME-Version: 1.0\\n"',
    '"Content-Type: text/plain; charset=UTF-8\\n"',
    "",
  ];
  for (const [id, str] of Object.entries(entries)) {
    lines.push(`msgid "${escapePo(id)}"`);
    lines.push(`msgstr "${escapePo(str)}"`);
    lines.push("");
  }
  fs.writeFileSync(path.join(localesDir, `${locale === "pirate" ? "pirate" : locale}.po`), lines.join("\n"), "utf8");
}

const existing = parsePo(fs.readFileSync(path.join(localesDir, "en.po"), "utf8"));
const en = { ...existing, ...EXTRA_EN };

// Ensure FR/PIRATE cover every English key (fallback to English if missing).
const fr = {};
const pirate = {};
for (const [id, str] of Object.entries(en)) {
  fr[id] = FR[id] ?? str;
  pirate[id] = PIRATE[id] ?? str;
}

writePo("en", en);
writePo("fr", fr);
writePo("pirate", pirate);
